public function getAllProductsActive(int $page): PaginationInterface
{
        $query = $this->createQueryBuilder('p')
            ->where('p.isActive = true')
            ->addOrderBy("p.id", "DESC")
            ->getQuery()
            ->getResult();
        $posts = $this->paginator->paginate(
            $query,
            $page,
            12
        );

        return $posts;
}

